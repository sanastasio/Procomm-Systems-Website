/**
 * Returns the correct document for the context
 */
var unifyDoc = function( t ) 
{
	var d = $( t.element )[0] || $( t )[0];
		d = d.URL || d.ownerDocument.URL;
	
	if( d != document.URL && typeof( ifrDoc ) != 'undefined' )
	{
		return ifrDoc;
	}
	else 
	{
		return document;
	}
};




